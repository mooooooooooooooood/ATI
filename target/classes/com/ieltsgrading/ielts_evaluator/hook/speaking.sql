USE ielts_db;


--- BẢNG 1: Lưu thông tin chung về bài kiểm tra
CREATE TABLE IF NOT EXISTS speaking_tests (
    test_id INT PRIMARY KEY AUTO_INCREMENT,
    test_date VARCHAR(50) NOT NULL, -- Ví dụ: 'Ngày 02.01'
    main_topic VARCHAR(255),        -- Tóm tắt các chủ đề chính (Part 1 & 2)
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

--- BẢNG 2: Lưu chi tiết Part 2 Cue Card và tóm tắt chủ đề Part 1
--- Part 2 Cue Card là một khối văn bản duy nhất (tiêu đề + gạch đầu dòng)
CREATE TABLE IF NOT EXISTS speaking_test_details (
    detail_id INT PRIMARY KEY AUTO_INCREMENT,
    test_id INT NOT NULL,
    part_topic VARCHAR(255) NOT NULL, -- Ví dụ: 'Part 1 Topics', 'Part 2 Cue Card'
    detail_text TEXT,                 -- Nội dung chi tiết (Cue Card hoặc Tóm tắt Part 1)
    -- FIX: Changed REFERENCES tests(test_id) to REFERENCES speaking_tests(test_id)
    FOREIGN KEY (test_id) REFERENCES speaking_tests(test_id)
        ON DELETE CASCADE
        ON UPDATE CASCADE
);

--- BẢNG 3: Lưu TRỪ TỪNG CÂU HỎI NHỎ (Part 1 và Part 3)
CREATE TABLE IF NOT EXISTS speaking_test_questions (
    question_id INT PRIMARY KEY AUTO_INCREMENT,
    test_id INT NOT NULL,
    part_number VARCHAR(10) NOT NULL,  -- 'Part 1' hoặc 'Part 3'
    question_text TEXT NOT NULL,       -- Nội dung từng câu hỏi
    -- FIX: Changed REFERENCES tests(test_id) to REFERENCES speaking_tests(test_id)
    FOREIGN KEY (test_id) REFERENCES speaking_tests(test_id)
        ON DELETE CASCADE
);
